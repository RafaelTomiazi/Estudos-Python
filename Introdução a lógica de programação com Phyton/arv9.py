"""
Escreva um programa que peça para o usuário dois números a e b. 
Depois disso o programa deve imprimir as seguintes linhas:

a = [valor_do_a]
b = [valor_do_b]
a + b = [valor_de_a+b]
a**b = [valor_de_a**b]
a - b = [valor_de_a-b]

Do lado direito do sinal de igual deve aparecer o valor referente a conta do que aparece do lado esquerdo. 
O exemplo do primeiro print já está feito.
"""

# recebe dados de entrada do usuário e converte para inteiro
a = int(input("Digite o valor de a: "))
b = int(input("Digite o valor de b: "))


print("a =", a)


print("b =", b)

print("a + b =", a + b)


print("a**b =", a**b)


print("a - b =", a - b)
